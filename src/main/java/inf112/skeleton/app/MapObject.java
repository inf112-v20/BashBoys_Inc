package inf112.skeleton.app;

public interface MapObject {

    /**
     * Get X coordinate
     */
    public void getX();

    /**
     * Get Y coordinate
     */
    public void getY();

    /**
     * Set X coordinate
     */
    public void setX();

    /**
     * Set Y coordinate
     */
    public void setY();

    /**
     * Remove object from map
     */
    public void remove();
}
