package inf112.skeleton.app;

public interface MapObject {

	/**
	 * Get X coordinate
	 */
	public int getX();

	/**
	 * Get Y coordinate
	 */
	public int getY();

	/**
	 * Set X coordinate
	 */
	public void setX();

	/**
	 * Set Y coordinate
	 */
	public void setY();

	/**
	 * Remove object from map
	 */
	public void remove();
}
