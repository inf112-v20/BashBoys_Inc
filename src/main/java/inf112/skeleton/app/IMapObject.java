package inf112.skeleton.app;

public interface IMapObject {

    /**
     * Get X coordinate
     */
     int getX();

    /**
     * Get Y coordinate
     */
     int getY();

    /**
     * Set X coordinate
     */
     void setX(int x);

    /**
     * Set Y coordinate
     */
     void setY(int y);


    /**
     * Return name
     * @return
     */
     String getName();
}
